let trafficLight = "pink";

switch (trafficLight) {
    case "red":
        console.log("The light is red. Stop!");
        break;
    case "yellow":
        console.log("The light is yellow. Slow down!");
        break;
    case "green":
        console.log("The light is green. Go!");
        break;
    default:
        console.log("The light is turned off.");
        break;
}

